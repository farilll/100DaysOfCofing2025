public class day026 {
    public static void main(String[] args) {
        int a = 3;

        a += 2;
        System.out.println(a);

        a -= 2;
        System.out.println(a);

        a *= 2;
        System.out.println(a);

        a /= 2;
        System.out.println(a);

        a %= 2;
        System.out.println(a);
    }
}