public class day058 {
    public static void main(String[] args) {

        int a = 10;

        for (int i = 1; i <= a; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        for (int i = a; i >= 1; i--) {
            System.out.print(i + " ");
        }
    }
}
